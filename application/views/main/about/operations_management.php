                    <h1 class="card-title">THE OPERATIONS, MANAGEMENT POLICIES AND PROCEDURES</h1>

                    <h4>Organization and Management</h4>
                    <p>The DILG Oriental Mindoro Provincial Director heads the Oriental
                        Mindoro PLGRC with the Cluster Team Leader and the Program Manager as
                        members. Through the various PLGRC facilities, they generally supervise
                        the center's overall activities. In conjunction with this, the Provincial
                        Director must give his or her approval to all activities funded by DILG and
                        lodged under the Oriental Mindoro PLGRC. In connection to this, all
                        activities lodged under the Oriental Mindoro PLGRC and funded under
                        DILG should be approved by the Provincial Director.
                    </p>
                    <p>The organization and management of the Oriental Mindoro PLGRC is
                        designed in such a way that will foster its core principles of
                        multi-stakeholdership, partnership, and convergence within the PLGRC
                        and among its MSAC members.</p>
                    <p>In accordance with the terms of the Memorandum of Agreement
                        (MOA) between partner institutions, the MSAC will act as an outside
                        advisory group that will offer advice and partnership on the plans,
                        programs, or activities of the PLGRC. The specific roles and contributions
                        of the MSAC members involved in the project shall be clearly stated in this
                        agreement.
                    </p>

                    <h4>Knowledge Management Process</h4>
                    <h5>Knowledge Creation/Acquisition:</h5>
                    <p>The PLGRC will make an effort to develop and gather explicit
                        and tacit knowledge. The following principles will aid in knowledge
                        creation and acquisition:
                    </p>
                    <ol>
                        <li>The PLGRC librarian is responsible for keeping and conducting
                            an inventory of knowledge products (KP).</li>
                        <li>KP inventory may include other knowledge products from
                            other DILG Offices, LGRCs, and MSAC members in addition to
                            the library of Oriental Mindoro PLGRC. Moreover, KPs of
                            partners and collaborators for referral reasons must be
                            included in the inventory.</li>
                        <li>The results of the KP inventory will provide a guide for
                            knowledge capital planning.</li>
                        <li>The PLGRC through the library shall acquire the following
                            types of knowledge products:
                            <ul>
                                <li>Manuals (such as CDP Preparation, ELA Formulation,
                                    CapDev Agenda preparation, GAD Plan and Budget, Peace and Order and Public Safety (POPS) Plan etc.
                                    and others)</li>
                                <li>Training Modules (relevant to local governance)</li>
                                <li>Laws/Policies/Guidelines on Local Governance</li>
                                <li>Local Plans such as CLUP, CDP, LDIP, ELA, DRRM Plans,
                                    GAD Plan, and Budget, LCCAP, other plans</li>
                                <li>Diagnostics Tools on Local Governance (such as
                                    SGLG-GAR, CBMS, LTIA, Great Barangay Award,
                                    Harmonized Roadmap Template (HRT), Peace and
                                    order Council Performance Audit, LADAC Functionality
                                    Assessment, BBIs Functionality Assessment, Training
                                    Need Assessments and others)</li>
                                <li>Quality Management System Manual</li>
                                <li>LGU Profiles and databases of Local Officials</li>
                                <li>Directories of MSAC members, LRI partners, CSO and
                                    NGO partners</li>
                                <li>Inventory of Pool of Experts</li>
                                <li>Infographics, brochures, flyers, pamphlets of various
                                    PPAs</li>
                                <li>AVP and Powerpoint presentations of various PPAs</li>
                                <li>Local Governance best practices (written and video)</li>
                                <li>DILG and other MSAC members Annual Reports and
                                    Newsletters</li>
                                <li>Systems and Procedures Manual (Operations Manual)</li>
                                <li>Local Governance (LG) Awards Systems</li>
                                <li>Other LG relevant data/information</li>
                            </ul>
                        </li>
                        <p>Knowledge products come in both hard copy and soft copy
                            formats, including books, periodicals, flyers, and other materials
                            (for e-library).</p>
                        <li>The PLGRC will act as a repository for knowledge products
                            that DILG employees have acquired during seminars or
                            training sessions. As a result, all DILG employees, especially
                            those working in the provincial offices, must deposit all
                            knowledge products, regardless of their format, to the
                            librarian of the PLGRC for storage. For reference, the
                            documents will be a part of the PLGRC Collection.
                        </li>
                        <li>The PLGRC shall enter into agreements with other
                            organizations, partners, or collaborators who are willing to
                            contribute their physical or digital resources. A web link
                            between the PLGRC and any available e-libraries from
                            partners or collaborators must be established. In the event
                            that it becomes required, the relevant legal framework will
                            control the online links. The Linkage Facility will find potential partners and, working with the IT Officer, establish the
                            conditions for online links.
                        </li>
                    </ol>

                    <h5>Knowledge Storing:</h5>
                    <p>Knowledge storage in the PLGRC shall be governed by the
                        following principles to guarantee that it will be accessible to all
                        users/clients:</p>
                    <ol>
                        <li>The PLGRC shall keep a physical library. It will also set up a
                            databank to house knowledge products. Moreover, a
                            database will be accessible to view processed data.
                            Specifically, DILG reports will be kept in the PLGRC database.</li>
                        <li>Knowledge products, including assessment tools and results,
                            LGU profiles, training modules, manuals, plans, and DILG
                            reports, shall be stored in the appropriate databank.</li>
                        <li>The PLGRC e-Library shall build an Integrated Library
                            Management System (ILMS). The organization and
                            administration of the knowledge products in the PLGRC are
                            referred to as the ILMS. The establishment of the ILMS will
                            be facilitated by the librarian working in conjunction with the
                            IT Officer. In order to set up the library system, they may ask
                            the MSAC members (who are academics) for help or consult
                            other experts.</li>
                        <li>Themes must be used to organize all KPs, including hard
                            copies and digital files (on and offline).</li>
                    </ol>

                    <h5>Knowledge Sharing/Exchange:</h5>
                    <p>To facilitate knowledge sharing and exchanges, the following
                        guidelines are set:</p>

                    <ol>
                        <li>The PLGRC shall foster communication and exchanges of
                            information among partners and collaborators through online
                            and on-site modalities. Sharing online can take place in
                            online meeting platforms, forums, or through web links
                            (e-libraries). Coaching and discussions with the proper DILG
                            program focal persons or program/project lead can be used
                            to share information locally.</li>
                        <li>Any knowledge products, including data and information that
                            has been processed, can be shared with clients or partners.
                            Nevertheless, confidentiality of the information should be
                            taken into account when necessary.</li>
                    </ol>

                    <h5>Knowledge Utilization:</h5>
                    <p>In general, the PLGRC's knowledge base shall be used for services,
                        expertise, and institutional advancement. The PLGRC, as a recipient
                        and user of knowledge, shall apply the following principles:
                    </p>

                    <ol>
                        <li>Professionals/experts can access knowledge products when
                            providing services to clients, such as conducting workshops
                            and training sessions.</li>
                        <li>The PLGRC can also use knowledge products for staff
                            development initiatives.</li>
                        <li>Knowledge products are also free and accessible to DILG staff
                            and employees and/or MSAC partners.</li>
                        <li>The DILG also encourages internal information sharing,
                            including issue lobbying, educational forums, case analyses,
                            etc. The knowledge shared is to be freely discussed or
                            addressed.</li>
                        <li>To facilitate the delivery of the PLGRC services, experts in the
                            PLGRC network are also encouraged to use knowledge to
                            create or develop more knowledge products (such as
                            modules and handouts).</li>
                        <li>Other forms of learning that can be compiled into fresh
                            knowledge products are also developed through the PLGRC
                            engagements/activities.</li>
                        <li>Knowledge and information elicited from the analysis of
                            performance assessments can also be packaged into
                            knowledge products.</li>
                    </ol>

                    <h5>Knowledge Evaluation</h5>
                    <p>To evaluate the effectiveness of the PLGRC in the delivery of
                        its services and the achievement of its objectives and targets, a
                        feedback mechanism shall be established alongside both internal
                        and external KM Audit processes.</p>

                    <h4>Internal KM Audit</h4>
                    <p>The PLGRC Oriental Mindoro KM Audit shall be facilitated
                        under the guidance of the LGRRC MIMAROPA and the Local
                        Government Academy (LGA). KM Audits shall be performed using
                        different tools such as but not limited to Online Evaluation Surveys
                        (PPTs), Key Informant Interviews (KII), and Focus Group
                        Discussions (FGD) with PLGRC Oriental Mindoro personnel and
                        selected MSAC members. Direct observation and the evaluation of
                        current documents (such as provincial orders, business plans, OPBs, etc.) are both included in the assessment process. The
                        information gathered is examined to produce the assessment
                        findings in the following areas.
                    </p>

                    <ol type="a">
                        <li>Leadership</li>
                        <li>Process</li>
                        <li>People</li>
                        <li>Technology</li>
                        <li>Knowledge Processes</li>
                        <li>Learning and Innovation</li>
                        <li>Knowledge Management Outcomes</li>
                    </ol>
