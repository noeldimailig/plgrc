                    <h4>Partnership and Collaboration</h4>
                    <p>In recognition that the PLGRC holds no monopoly of knowledge in
                        the province, the Center aims to assist partner organizations and clients
                        by connecting them to appropriate organizations/agencies with the
                        capacity or mandate to help them. This can be in the form of pooling of
                        resources, tapping of subject matter experts and provision of technical
                        assistance, among others. The PLGRC serves as a broker by linking the
                        needs of its clients to the resources that are appropriate and necessary to
                        address their demands and needs.</p>
                    <p>Collaborations within the MSAC are deeply anchored on mutual
                        interests of member institutions and organizations to further excellence in
                        local governance in the province. The sharing of resources both logistical
                        and financial shall be based on the capacity of partnering MSAC members
                        to sustain collaborative programs, projects and activities.</p>

                    <h4>Resource Mobilization</h4>
                    <p>A significant aspect of the Oriental Mindoro PLGRC is resource
                        mobilization to be able to support and sustain its operations. Resources
                        can be in the form of financial resources and other logistical contributions
                        such as manpower hours, supplies, equipment, venue, and others. In
                        consideration of this, the Oriental Mindoro PLGRC may also adopt resource
                        generation strategies which includes seeking of grants, donations,
                        cost-sharing schemes or counterparting with partners, and developing
                        resource generating projects and activities. These strategies however
                        should exclude loans and other forms of indebtedness. These strategies
                        shall be incorporated in the business plan of Oriental Mindoro PLGRC.
                    </p>

                    <h4>Knowledge Sharing Mechanism</h4>
                    <p>The PLGRC operates on the principle of Knowledge Management
                        (KM) whose essential feature is the sharing of knowledge to the public.
                        Knowledge dissemination and sharing as an intermediate step in the KM
                        process endeavors public education and citizen empowerment.</p>
