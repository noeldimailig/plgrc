                    <h1 class="card-title">ORGANIZATION AND GOVERNANCE</h1>
                    <p>While being made up of multiple independent entities, the Provincial Local
                        Government Resource Center (PLGRC) of Oriental Mindoro has a straightforward
                        structure and management. The PLGRC Manager with the MSAC
                        (Multi-Stakeholder Advisory Committee) is at the top of the organizational
                        structure, followed by the PLGRC facilities. Due to the center's operational
                        concerns being managed by the PLGRC, the MSAC solely operated as an advisory
                        council.</p>
                    <p>The organizational structure of PLGRC includes membership of NGAs,
                        NGOs, POs, LGUs, Local Leagues, and LRIs that are crucial to social, economic,
                        environmental, and local governance. These organizations shared a common
                        interest of transferring knowledge and technology to its clients in order to
                        transform skills and knowledge into practical applications through convergence
                        towards developing excellent local governments.</p>
                    <p>The PLGRC is run on the principles of shared accountability and mutual
                        trust. It implies that the members are accountable for the accuracy of the data
                        collected and shared through its facilities. Moreover, anchored on the value of
                        transparency, members are provided with regular updates and reports on the
                        facilities’ operations in relation to the metrics used to measure outcomes.
                    </p>

                    <h3>ORGANIZATIONAL STRUCTURE</h3>
                    <img class="img-fluid" src="<?php echo base_url(); ?>assets/org-structure-lgrc.png" alt="Organizational Structure LGRC">
                    <p>The PLGRC runs within the fundamentals of partnership and cooperation.
                        Partnership refers to agreement wherein the parties work together to pursue
                        their mutual interests. All members are responsible for the organization’s
                        operations and outcomes, thus, it is essential that the capacities of member
                        organizations in terms of human resources, infrastructure and financial assets
                        are utilized strategically to meet the goals of convergence.</p>
                    <p>The Multi-Stakeholder Advisory Committee (MSAC) composed of
                        collaborators from governmental and non-governmental institutions shall provide
                        the overall direction of the PLGRC. The different facilities operating under the center shall provide support to its operations under the guidance of the MSAC.</p>
                    <p>The DILG Oriental Mindoro through the PLGRC Manager shall oversee the
                        operations of the PLGRC. The management of the center shall carry out plans
                        and programs with the concurrence of the MSAC. As a result, the management
                        has independence in developing policies, choosing interventions, and observing
                        the outcomes of LGRRC operation. The PLGRC designates a PLGRC Coordinator
                        who oversees the center's daily operations and works with the facilities.
                        Periodically, the MSAC receives reports from the PLGRC management</p>

