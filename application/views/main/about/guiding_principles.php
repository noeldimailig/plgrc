                        through proactive policy issuances on its operations, management, and
                        organization.</p>
                    <p>The Provincial Order organizing the Oriental Mindoro PLGRC shall be
                        updated based on the demands of the center. There shall also be capacity
                        development activities supportive of strengthening Knowledge
                        Management. The expansion of membership of the PLGRC and the MSAC
                        shall also be constantly reviewed to sustain its operations and deliver its
                        services.</p>
