                    <h3>Composition/Membership</h3>

                    <p>This MSAC is chaired by the DILG Provincial Director, with
                        heads of partner organizations, institutions and agencies as
                        members such as but not limited to (see attached List of MSAC
                        Members).
                    </p>

                    <h3>Functions and Responsibilities</h3>

                    <ul>
                        <li>Advise the Oriental Mindoro Local Governance Resource
                            Center on policies and strategic directions to improve its
                            operations to include the development of knowledge products
                            and services;</li>
                        <li>Provide technical assistance to other stakeholders on
                            knowledge management;</li>
                        <li>Share knowledge products that are classified as open
                            documents related to local governance to the LGRC and its
                            multi-stakeholders;</li>
                        <li>Identify and propose opportunities to strengthen convergence
                            and harmonize various programs of regional line agencies
                            and other institutions that would contribute in developing
                            local governments’ capacities;</li>
                        <li>Provide resource persons for various capability training
                            programs related to local governance; and</li>
                        <li>Meet regularly or as the need arises to assess the operations
                            and ensure the dynamism of the LGRC and its
                            responsiveness to the information/learning needs of the
                            region.</li>
                    </ul>

                    <h3>Channels of Communication</h3>
                    <p>The most crucial element for an organization to maintain its
                        relationship and carry out its activities successfully is
                        communication. It is an indispensable component of MSAC’s
                        operations, thus, communication is key to the success of its
                        activities. Guided by this principle, the MSAC is open to all forms of
                        communication as a crucial component of creating a positive
                        environment during the PLGRC operation, hence the establishment
                        of the hereunder communication protocols:
                    </p>
                    <ul>
                        <li>The MSAC Chair shall initiate council meetings. However, any
                            MSAC member may request the chair to an audience with the
                            full council or any concerned MSAC members. Meeting
                            advisories shall be issued by the MSAC chair.</li>
                        <li>The MSAC secretariat will facilitate communication to and
                            from the Chair, members and the PLGRC management.</li>
                        <li>The secretariat will maintain the database of
                            communications.</li>
                        <li>The MSAC members, the PLGRC Facilities and the PLGRC
                            Management may also communicate directly to each other;
                            provided, however, that their agreement will be formally
                            communicated if implemented.
                        </li>
                    </ul>

                    <h3>Meetings and Quorum</h3>

                    <p>The MSAC shall meet once every semester. Nonetheless, if
                        the need arises, the MSAC chair may schedule special meetings in
                        addition to the regular meetings. At these meetings, the secretariat
                        shall facilitate information flow and offer administrative assistance.
                        As often as necessary, the facilities may organize special MSAC
                        meetings subject to the approval of the MSAC Chair.</p>

                    <p>A quorum must be present in order to make the MSAC
                        meeting valid. A quorum is constituted when 50% plus one of the
                        total MSAC membership is reached. However, in emergency
                        situations and/or the exigency of service, the chair has the
                        discretion to call to order the meeting with or without quorum. In
                        both cases, the established process of discussing the business of
                        the day shall follow.
                    </p>

                    <p>The majority rule applies in all decision making.</p>

                    <h3>MSAC Secretariat</h3>
                    <p>The MSAC secretariat shall be the members of the Linkage
                        Facility. Its functions shall include the following:</p>
                    <ul>
                        <li>Maintaining database for the meeting, conferences, directory
                            and other information pertaining to the activities of the
                            committee.</li>
                        <li>Coordinates with other committee members.</li>
                        <li>Tracks of committee activities and updates members on the
                            Oriental Mindoro PLGRC operation.</li>
                        <li>Records the minutes of meetings, prepares resolutions,
                            memoranda, and other pertinent documents when applicable.</li>
                        <li>Maintain an updated directory of members and pool of
                            experts.</li>
                        <li>Other tasks as required and requested by the committee.</li>
                        </li>
                    </ul>
